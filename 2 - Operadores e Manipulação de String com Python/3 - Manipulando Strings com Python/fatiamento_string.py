nome = "Guilherme Arthur de Carvalho"

print(nome[0]) # Recortando por indice, nesse caso a primeira letra
print(nome[:9]) # Recortando do primeiro elemento  (:) até o 9
print(nome[-1])
print(nome[10:]) # Recortando do 10 elemento até o final
print(nome[10:16]) # Recortando do 10 elemento até o 16
print(nome[10:16:2]) # Recortando do 10 elemento até o 16, porém de 2 em 2 elementos
print(nome[:]) # Retorna uma cópia da string
print(nome[::-1]) # Retorna a string original espelhada